our_string = 'Anant upadhyay goes to a PICNIC one day.'

print(our_string.index('y')) # Finding the index of the first occurence of the character 'y' in our_string.
# 11

print(our_string.swapcase()) # Swapping the case of the characters in our_string.
# aNANT UPADHYAY GOES TO A picnic ONE DAY.

print(our_string.title()) # Capitalizing the first letter of each word in our_string.
# Anant Upadhyay Goes To A Picnic One Day.

print(our_string.capitalize()) # Capitalizing the first letter of the first word in our_string.
# Anant upadhyay goes to a picnic one day.

print(our_string.startswith('Anant')) # Checking if our_string starts with the string 'Anant'.
# True

print(our_string.endswith('Anant')) # Checking if our_string ends with the string 'Anant'.
# False
