our_string = 'Hi, my name is Anant!'

print(our_string)
# Hi, my name is Anant!

print(our_string[0]) # Getting the character at Index 0.
# H

print(our_string[4:]) # Fetching the sliced string starting from Index 4 going till the end of the string. 
# my name is Anant!

print(our_string[4:11])
# my name

print(our_string[1:14:2]) # Slicing the string, starting with Index 1 with 2 steps ending at but not including Index 14.
# i ynm s

print(our_string[-6:-1])
# Anant

print(our_string[::-1]) # Commonly used to reverse the string.
# !tnanA si eman ym ,iH
