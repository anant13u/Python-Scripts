# An integer is a whole number, and a float is a decimal. Difference between Ints and Floats:
# Integer - 4, 57, -10
# Floating Point - 6.1, 1.3333, 0.0

print (7/3)
# 2.3333333333333335
print (7*3)
# 21
print(7+3)
# 10
print(7-3)
# 4


# Integer Division - Gives the result of division in Int format after rounding down:

print (7//3) #Floor Division - Removes the remainder after division
# 2
print (7**3) #Exponent
# 343


# Mod (Modulo) - Gives the remainder:

print (8%4) #Modulus
# 0
print (9%4) #Modulus
# 1
print (10%4) #Modulus
# 2
print (11%4) #Modulus
# 3

print(7>3)
# True
print(7<3)
# False
print(7!=3) #Not Equal sign
# True
print (4>=4)
# True
print (2>=4)
# False
